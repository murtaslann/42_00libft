#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	size_t			i;
	unsigned char	*dst1;
	unsigned char	*src1;

	dst1 = (unsigned char *)dst;
	src1 = (unsigned char *)src;
	i = 0;
	if (src == dst && n != 0)
		return (dst);
	while (i < n)
	{
		*dst1++ = *src1++;
		n--;
	}
	return ((unsigned char *)dst);
}
